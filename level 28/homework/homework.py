text = "hello world"
upper_text = text.upper()
print(upper_text)
# With "upper case", we can make any string have bigger letters.

text1 = "HELLO WORLD"
lower_text = text1.lower()
print(lower_text)
#lower case is opposite of upper one, it makes big letters to be small.
text2 = "hello world"
capitalized_text = text2.capitalize()
print(capitalized_text)
#"capitalized case" starts sentence with big letter.
text3 = "hElLo WoRlD"
swapped_text = text3.swapcase()
print(swapped_text)
#"swapcase" makes big letters smaller and small letters bigger.
