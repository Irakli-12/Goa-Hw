# 2) შექმენით რაიმე რიცხვების list'ი და map'ის გამოყენებით ყველა მნიშვნელობა გაამრავლეთ ორზე

list1 = [1, 2, 3, 4]
result = map(lambda x:x *2,list1)
print(list(result))

